package com.library.book.domain.model.bookcopy;

import jakarta.persistence.Embeddable;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NoArgsConstructor;

import java.io.Serializable;

@Embeddable
@Getter
@EqualsAndHashCode
@NoArgsConstructor
public class BookCopyId implements Serializable {
    private Long value;
    
    public BookCopyId(Long value) {
        this.value = value;
    }
    
    public static BookCopyId createNew() {
        return new BookCopyId(null); // ID will be generated by DB
    }
    
    public static BookCopyId of(Long id) {
        return new BookCopyId(id);
    }
    
    @Override
    public String toString() {
        return value != null ? value.toString() : "new";
    }
} 